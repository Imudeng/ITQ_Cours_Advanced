package lesson_05_anonymos_inner_class;

public class Cat {
    private String name;

    public Cat(String name) {
        this.name = name;
    }

    public Cat() {

    }

    public String getVoice() {
        return "Meow";
    }

    public String toString() {
        return "Cat [name=" + name + "]";

    }

}
